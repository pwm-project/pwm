package password.pwm.bean;

import java.io.Serializable;
import java.util.Date;
import java.util.Locale;

public class SessionStateInfoBean implements Serializable {
    private String label;
    private Date createTime;
    private Date lastTime;
    private String idle;
    private Locale locale;
    private String ldapProfile;
    private String userDN;
    private String userID;
    private String srcAddress;
    private String srcHost;
    private String lastUrl;
    private int intruderAttempts;

    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }

    public Date getCreateTime() {
        return createTime;
    }

    public void setCreateTime(Date createTime) {
        this.createTime = createTime;
    }

    public Date getLastTime() {
        return lastTime;
    }

    public void setLastTime(Date lastTime) {
        this.lastTime = lastTime;
    }

    public String getIdle() {
        return idle;
    }

    public void setIdle(String idle) {
        this.idle = idle;
    }

    public Locale getLocale() {
        return locale;
    }

    public void setLocale(Locale locale) {
        this.locale = locale;
    }

    public String getLdapProfile() {
        return ldapProfile;
    }

    public void setLdapProfile(String ldapProfile) {
        this.ldapProfile = ldapProfile;
    }

    public String getUserDN() {
        return userDN;
    }

    public void setUserDN(String userDN) {
        this.userDN = userDN;
    }

    public String getUserID() {
        return userID;
    }

    public void setUserID(String userID) {
        this.userID = userID;
    }

    public String getSrcAddress() {
        return srcAddress;
    }

    public void setSrcAddress(String srcAddress) {
        this.srcAddress = srcAddress;
    }

    public String getSrcHost() {
        return srcHost;
    }

    public void setSrcHost(String srcHost) {
        this.srcHost = srcHost;
    }

    public String getLastUrl() {
        return lastUrl;
    }

    public void setLastUrl(String lastUrl) {
        this.lastUrl = lastUrl;
    }

    public int getIntruderAttempts() {
        return intruderAttempts;
    }

    public void setIntruderAttempts(int intruderAttempts) {
        this.intruderAttempts = intruderAttempts;
    }
}
